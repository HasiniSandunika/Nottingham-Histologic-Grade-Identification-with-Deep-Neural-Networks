    private void sdwbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sdwbtnDeleteActionPerformed
        try {
            BasicDBObject ob = new BasicDBObject();
            ob.put("date_time", dateTime);
            DBCursor cursor = Connector.con.find(ob);
            String st = null;
            while (cursor.hasNext()) {
                st = cursor.next().toString();
            }
            if (st == null) {
                JOptionPane.showMessageDialog(null, "No such record is found to remove!", "Error",
                        JOptionPane.ERROR_MESSAGE);
            } else {
                Connector.con.remove(ob);
                this.setVisible(false);
                new SecondWindow().setVisible(true);               
                JOptionPane.showMessageDialog(null, "Successfully removed the record!");               
            }
        } catch (HeadlessException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "An error occured while deleting!", "Error", JOptionPane.ERROR_MESSAGE);
            java.util.logging.Logger.getLogger(SearchDataWindow.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_sdwbtnDeleteActionPerformed

