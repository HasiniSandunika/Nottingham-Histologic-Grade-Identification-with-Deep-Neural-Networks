    private void setImageIcon() {
        try {
            byte[] imageBytes = javax.xml.bind.DatatypeConverter.parseBase64Binary(imageBase64);
            ByteArrayInputStream bis = new ByteArrayInputStream(imageBytes);
            BufferedImage bImage = ImageIO.read(bis);
            Image newImg = new ImageIcon(bImage).getImage().getScaledInstance(sgwlblImage.getWidth(), sgwlblImage.getHeight(), Image.SCALE_SMOOTH);
            ImageIcon image = new ImageIcon(newImg);
            sgwlblImage.setIcon(image);
        } catch (IOException ex) {
            System.out.println(ex);
            JOptionPane.showMessageDialog(null, "An error occured while retrieving data!", "Error", JOptionPane.ERROR_MESSAGE);
            java.util.logging.Logger.getLogger(SearchDataWindow.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }

